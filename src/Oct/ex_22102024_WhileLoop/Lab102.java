package Oct.ex_22102024_WhileLoop;

public class Lab102 {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10){
            // Code
            System.out.println(i);
            i++;
        }

        for (int j = 0; j < 10; j++) {
            System.out.println(j);
        }
    }
}
