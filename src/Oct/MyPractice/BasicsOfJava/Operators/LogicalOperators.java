package Oct.MyPractice.BasicsOfJava.Operators;

public class LogicalOperators {
    public static void main(String[] args) {

        boolean k = true, q = false;
        System.out.println("k && q: " + (k && q));
        System.out.println("k || q: " + (k || q));
        System.out.println("!k: " + (!k));

    }
}
