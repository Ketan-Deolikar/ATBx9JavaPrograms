package Oct.MyPractice.BasicsOfJava.Operators;

public class UnaryOperators {
    public static void main(String[] args) {

        int u = 10;
        System.out.println("+u: " + (+u));
        System.out.println("-u: " + (-u));
        System.out.println("u++: " + (u++));
        System.out.println("++u: " + (++u));
    }
}
